-- =============================================================================
-- Скрипт генерации тестовых данных для нагрузочного тестирования
-- Справочная БД AlphaMeterQC
-- СУБД: PostgreSQL 15+
-- Автор: В.Л. Солодюк
-- =============================================================================
-- Скрипт генерирует тестовые данные в масштабах, приближенных к реальной
-- эксплуатации: до 10 000 записей в таблицах ТочкаУчёта, Заявка, Примечание.
-- =============================================================================

SET search_path TO справочная_бд;

-- ---------------------------------------------------------------------------
-- 1. Генерация дополнительных организаций (до 100)
-- ---------------------------------------------------------------------------
INSERT INTO Организация (Наименование, ПризнакПотребителя)
SELECT
    'ООО "Тестовая организация ' || n || '"',
    (random() < 0.7)  -- 70% — потребители
FROM generate_series(11, 100) AS n;

-- ---------------------------------------------------------------------------
-- 2. Генерация дополнительных структурных подразделений (до 500)
-- ---------------------------------------------------------------------------
INSERT INTO СтруктурноеПодразделение (ИД_Организации, Наименование)
SELECT
    (random() * 99 + 1)::INTEGER,
    'Подразделение-' || n
FROM generate_series(11, 500) AS n;

-- ---------------------------------------------------------------------------
-- 3. Генерация дополнительных контактных лиц (до 1 000)
-- ---------------------------------------------------------------------------
INSERT INTO КонтактноеЛицо (ИД_Подразделения, Фамилия, Имя, Отчество, Должность, Телефон, Email)
SELECT
    (random() * 499 + 1)::INTEGER,
    'Фамилия-' || n,
    'Имя-' || n,
    'Отчество-' || n,
    CASE (random() * 4)::INTEGER
        WHEN 0 THEN 'Инженер'
        WHEN 1 THEN 'Начальник отдела'
        WHEN 2 THEN 'Ведущий специалист'
        WHEN 3 THEN 'Администратор'
        ELSE 'Руководитель'
    END,
    '+7 (3494) ' || (10000 + n)::TEXT,
    'contact' || n || '@test-org.ru'
FROM generate_series(11, 1000) AS n;

-- ---------------------------------------------------------------------------
-- 4. Генерация дополнительных мест расположения (до 200)
-- ---------------------------------------------------------------------------
INSERT INTO МестоРасположения (Наименование)
SELECT
    'г. Новый Уренгой, ул. Тестовая, д. ' || n
FROM generate_series(11, 200) AS n;

-- ---------------------------------------------------------------------------
-- 5. Генерация дополнительных объектов (до 1 000)
-- ---------------------------------------------------------------------------
INSERT INTO Объект (Наименование, НомерВБДАльфаЦЕНТР, ИД_МестаРасположения, ИД_Подразделения)
SELECT
    'Тестовый объект-' || n,
    'OBJ-TEST-' || LPAD(n::TEXT, 5, '0'),
    (random() * 199 + 1)::INTEGER,
    (random() * 499 + 1)::INTEGER
FROM generate_series(11, 1000) AS n;

-- ---------------------------------------------------------------------------
-- 6. Генерация дополнительных точек учёта (до 10 000)
-- ---------------------------------------------------------------------------
INSERT INTO ТочкаУчёта (Наименование, НомерПрисоединения, ИД_Объекта, ИД_Статуса)
SELECT
    'ТУ Тестовый объект-' || (n / 10 + 1) || ' Ввод ' || (n % 10 + 1),
    'FDR-TEST-' || LPAD(n::TEXT, 6, '0'),
    (random() * 999 + 1)::INTEGER,
    (random() * 5 + 1)::INTEGER
FROM generate_series(11, 10000) AS n;

-- ---------------------------------------------------------------------------
-- 7. Генерация дополнительных заявок (до 30 000)
-- ---------------------------------------------------------------------------
INSERT INTO Заявка (ИД_ТУ, НомерЗаявки, ДатаНачала, ДатаОкончания)
SELECT
    (random() * 9999 + 1)::INTEGER,
    'З-ТЕСТ-' || LPAD(n::TEXT, 7, '0'),
    DATE '2024-01-01' + (random() * 730)::INTEGER,
    CASE WHEN random() < 0.6
        THEN DATE '2024-01-01' + (random() * 730)::INTEGER
        ELSE NULL
    END
FROM generate_series(11, 30000) AS n;

-- ---------------------------------------------------------------------------
-- 8. Генерация дополнительных примечаний (до 30 000)
-- ---------------------------------------------------------------------------
INSERT INTO Примечание (ИД_ТУ, Текст)
SELECT
    (random() * 9999 + 1)::INTEGER,
    'Тестовое примечание №' || n || '. ' ||
    CASE (random() * 4)::INTEGER
        WHEN 0 THEN 'Требуется плановая поверка оборудования.'
        WHEN 1 THEN 'Выявлено отклонение показаний. Направлена заявка на внеплановую проверку.'
        WHEN 2 THEN 'Оборудование работает в штатном режиме. Отклонений не выявлено.'
        WHEN 3 THEN 'Планируется замена счётчика в рамках программы модернизации.'
        ELSE 'Требуется уточнение схемы подключения. Направлен запрос в службу эксплуатации.'
    END
FROM generate_series(11, 30000) AS n;

-- ---------------------------------------------------------------------------
-- 9. Подтверждение успешной генерации
-- ---------------------------------------------------------------------------
DO $$
DECLARE
    cnt_org   INTEGER;
    cnt_sub   INTEGER;
    cnt_cont  INTEGER;
    cnt_loc   INTEGER;
    cnt_obj   INTEGER;
    cnt_tu    INTEGER;
    cnt_stat  INTEGER;
    cnt_zayav INTEGER;
    cnt_primech INTEGER;
BEGIN
    SELECT COUNT(*) INTO cnt_org   FROM Организация;
    SELECT COUNT(*) INTO cnt_sub   FROM СтруктурноеПодразделение;
    SELECT COUNT(*) INTO cnt_cont  FROM КонтактноеЛицо;
    SELECT COUNT(*) INTO cnt_loc   FROM МестоРасположения;
    SELECT COUNT(*) INTO cnt_obj   FROM Объект;
    SELECT COUNT(*) INTO cnt_tu    FROM ТочкаУчёта;
    SELECT COUNT(*) INTO cnt_stat  FROM СтатусТУ;
    SELECT COUNT(*) INTO cnt_zayav FROM Заявка;
    SELECT COUNT(*) INTO cnt_primech FROM Примечание;

    RAISE NOTICE 'Генерация тестовых данных завершена.';
    RAISE NOTICE '  Организации:                %', cnt_org;
    RAISE NOTICE '  Структурные подразделения:  %', cnt_sub;
    RAISE NOTICE '  Контактные лица:            %', cnt_cont;
    RAISE NOTICE '  Места расположения:         %', cnt_loc;
    RAISE NOTICE '  Объекты:                    %', cnt_obj;
    RAISE NOTICE '  Точки учёта:                %', cnt_tu;
    RAISE NOTICE '  Статусы ТУ:                 %', cnt_stat;
    RAISE NOTICE '  Заявки:                     %', cnt_zayav;
    RAISE NOTICE '  Примечания:                 %', cnt_primech;
END;
$$;