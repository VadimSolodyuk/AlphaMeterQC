#!/bin/bash
# =============================================================================
# Скрипт восстановления справочной БД AlphaMeterQC из резервной копии
# СУБД: PostgreSQL 15+
# Автор: В.Л. Солодюк
# =============================================================================
# Скрипт восстанавливает БД alphameterqc из указанной резервной копии
# в custom-формате (pg_dump --format=custom).
# =============================================================================

set -e

# =============================================================================
# Конфигурация
# =============================================================================

# Параметры подключения к БД
DB_HOST="localhost"
DB_PORT="5432"
DB_NAME="alphameterqc"
DB_USER="postgres"

# Файл резервной копии (передаётся как аргумент)
BACKUP_FILE="$1"

# Файл журнала
LOG_FILE="/backup/alphameterqc/restore_$(date +%Y%m%d_%H%M%S).log"

# =============================================================================
# Функции
# =============================================================================

log() {
    echo "[$(date '+%Y-%m-%d %H:%M:%S')] $1" | tee -a "$LOG_FILE"
}

usage() {
    echo "Использование: $0 <файл_резервной_копии>"
    echo "Пример: $0 /backup/alphameterqc/alphameterqc_20260504_120000.dump"
    exit 1
}

# =============================================================================
# Основной блок
# =============================================================================

# Проверка аргументов
if [ -z "$BACKUP_FILE" ]; then
    echo "ОШИБКА: Не указан файл резервной копии."
    usage
fi

if [ ! -f "$BACKUP_FILE" ]; then
    echo "ОШИБКА: Файл '$BACKUP_FILE' не найден."
    exit 1
fi

log "=== Начало восстановления ${DB_NAME} ==="
log "Файл резервной копии: ${BACKUP_FILE}"

# Проверка подключения к БД
if ! pg_isready -h "$DB_HOST" -p "$DB_PORT" > /dev/null 2>&1; then
    log "ОШИБКА: PostgreSQL не доступен на ${DB_HOST}:${DB_PORT}"
    exit 1
fi
log "Подключение к PostgreSQL установлено."

# Завершение активных подключений к БД
log "Завершение активных подключений к ${DB_NAME}..."
psql -h "$DB_HOST" -p "$DB_PORT" -U "$DB_USER" -d postgres \
    -c "SELECT pg_terminate_backend(pid) FROM pg_stat_activity WHERE datname = '${DB_NAME}' AND pid <> pg_backend_pid();" \
    >> "$LOG_FILE" 2>&1 || true

# Восстановление из резервной копии
log "Выполнение pg_restore..."
pg_restore -h "$DB_HOST" -p "$DB_PORT" -U "$DB_USER" -d "$DB_NAME" \
    --clean \
    --if-exists \
    --verbose \
    "$BACKUP_FILE" 2>> "$LOG_FILE"

# Проверка результата
if [ $? -eq 0 ]; then
    log "Восстановление успешно завершено."
    log "База данных ${DB_NAME} восстановлена из: ${BACKUP_FILE}"
else
    log "ОШИБКА: Восстановление не удалось."
    log "Проверьте журнал: ${LOG_FILE}"
    exit 1
fi

log "=== Восстановление завершено ==="
exit 0