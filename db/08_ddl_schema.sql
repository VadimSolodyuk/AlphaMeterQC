-- ---------------------------------------------------------------------------
-- 2. Создание схемы (опционально, для логической группировки объектов)
-- ---------------------------------------------------------------------------
CREATE SCHEMA IF NOT EXISTS справочная_бд;
SET search_path TO справочная_бд;

-- ===========================================================================
-- 3. Таблица: Организация
--    Хранит юридические лица, которые могут являться Потребителями
--    электрической энергии.
-- ===========================================================================
CREATE TABLE Организация (
    ИД_Организации   INTEGER      GENERATED ALWAYS AS IDENTITY
                                   PRIMARY KEY,
    Наименование     VARCHAR(255) NOT NULL,
    ПризнакПотребителя BOOLEAN    DEFAULT FALSE
);

COMMENT ON TABLE  Организация IS 'Юридические лица — организации и потребители';
COMMENT ON COLUMN Организация.ИД_Организации IS 'Уникальный идентификатор организации (суррогатный ПК)';
COMMENT ON COLUMN Организация.Наименование IS 'Полное наименование организации';
COMMENT ON COLUMN Организация.ПризнакПотребителя IS 'Флаг: TRUE — организация является потребителем электроэнергии';

-- ===========================================================================
-- 4. Таблица: СтруктурноеПодразделение
--    Хранит подразделения организаций, в эксплуатационной ответственности
--    которых находятся объекты.
-- ===========================================================================
CREATE TABLE СтруктурноеПодразделение (
    ИД_Подразделения INTEGER      GENERATED ALWAYS AS IDENTITY
                                   PRIMARY KEY,
    ИД_Организации   INTEGER      NOT NULL
                                   REFERENCES Организация(ИД_Организации)
                                   ON DELETE CASCADE,
    Наименование     VARCHAR(255) NOT NULL
);

COMMENT ON TABLE  СтруктурноеПодразделение IS 'Структурные подразделения организаций';
COMMENT ON COLUMN СтруктурноеПодразделение.ИД_Подразделения IS 'Уникальный идентификатор подразделения (суррогатный ПК)';
COMMENT ON COLUMN СтруктурноеПодразделение.ИД_Организации IS 'Ссылка на организацию (ВК → Организация)';
COMMENT ON COLUMN СтруктурноеПодразделение.Наименование IS 'Наименование подразделения';

CREATE INDEX idx_подразделение_организация
    ON СтруктурноеПодразделение(ИД_Организации);

-- ===========================================================================
-- 5. Таблица: КонтактноеЛицо
--    Хранит контактных лиц структурных подразделений.
-- ===========================================================================
CREATE TABLE КонтактноеЛицо (
    ИД_КонтактногоЛица INTEGER     GENERATED ALWAYS AS IDENTITY
                                    PRIMARY KEY,
    ИД_Подразделения   INTEGER     NOT NULL
                                    REFERENCES СтруктурноеПодразделение(ИД_Подразделения)
                                    ON DELETE CASCADE,
    Фамилия            VARCHAR(100) NOT NULL,
    Имя                VARCHAR(100),
    Отчество           VARCHAR(100),
    Должность          VARCHAR(255),
    Телефон            VARCHAR(50),
    Email              VARCHAR(255)
);

COMMENT ON TABLE  КонтактноеЛицо IS 'Контактные лица структурных подразделений';
COMMENT ON COLUMN КонтактноеЛицо.ИД_КонтактногоЛица IS 'Уникальный идентификатор контактного лица (суррогатный ПК)';
COMMENT ON COLUMN КонтактноеЛицо.ИД_Подразделения IS 'Ссылка на структурное подразделение (ВК → СтруктурноеПодразделение)';
COMMENT ON COLUMN КонтактноеЛицо.Фамилия IS 'Фамилия (обязательное поле)';
COMMENT ON COLUMN КонтактноеЛицо.Имя IS 'Имя';
COMMENT ON COLUMN КонтактноеЛицо.Отчество IS 'Отчество';
COMMENT ON COLUMN КонтактноеЛицо.Должность IS 'Должность';
COMMENT ON COLUMN КонтактноеЛицо.Телефон IS 'Контактный телефон';
COMMENT ON COLUMN КонтактноеЛицо.Email IS 'Адрес электронной почты';

CREATE INDEX idx_контактноелицо_подразделение
    ON КонтактноеЛицо(ИД_Подразделения);

-- ===========================================================================
-- 6. Таблица: МестоРасположения
--    Хранит географические места расположения объектов/электроустановок.
-- ===========================================================================
CREATE TABLE МестоРасположения (
    ИД_МестаРасположения INTEGER      GENERATED ALWAYS AS IDENTITY
                                       PRIMARY KEY,
    Наименование         VARCHAR(255) NOT NULL
);

COMMENT ON TABLE  МестоРасположения IS 'Места расположения объектов/электроустановок';
COMMENT ON COLUMN МестоРасположения.ИД_МестаРасположения IS 'Уникальный идентификатор места расположения (суррогатный ПК)';
COMMENT ON COLUMN МестоРасположения.Наименование IS 'Наименование места расположения';

-- ===========================================================================
-- 7. Таблица: Объект
--    Хранит объекты/электроустановки, в пределах которых осуществляется
--    учёт электрической энергии.
-- ===========================================================================
CREATE TABLE Объект (
    ИД_Объекта            INTEGER      GENERATED ALWAYS AS IDENTITY
                                       PRIMARY KEY,
    Наименование          VARCHAR(255) NOT NULL,
    НомерВБДАльфаЦЕНТР   VARCHAR(50)  NOT NULL,
    ИД_МестаРасположения  INTEGER      NOT NULL
                                       REFERENCES МестоРасположения(ИД_МестаРасположения)
                                       ON DELETE RESTRICT,
    ИД_Подразделения      INTEGER      NOT NULL
                                       REFERENCES СтруктурноеПодразделение(ИД_Подразделения)
                                       ON DELETE RESTRICT,
    CONSTRAINT uq_объект_номер_альфацентр UNIQUE (НомерВБДАльфаЦЕНТР)
);

COMMENT ON TABLE  Объект IS 'Объекты/электроустановки';
COMMENT ON COLUMN Объект.ИД_Объекта IS 'Уникальный идентификатор объекта (суррогатный ПК)';
COMMENT ON COLUMN Объект.Наименование IS 'Наименование объекта/электроустановки';
COMMENT ON COLUMN Объект.НомерВБДАльфаЦЕНТР IS 'Уникальный номер объекта в БД АльфаЦЕНТР';
COMMENT ON COLUMN Объект.ИД_МестаРасположения IS 'Ссылка на место расположения (ВК → МестоРасположения)';
COMMENT ON COLUMN Объект.ИД_Подразделения IS 'Ссылка на подразделение-эксплуатанта (ВК → СтруктурноеПодразделение)';

CREATE INDEX idx_объект_месторасположения
    ON Объект(ИД_МестаРасположения);
CREATE INDEX idx_объект_подразделение
    ON Объект(ИД_Подразделения);
CREATE INDEX idx_объект_номер_альфацентр
    ON Объект(НомерВБДАльфаЦЕНТР);

-- ===========================================================================
-- 8. Таблица: СтатусТУ
--    Справочник статусов точек учёта с нормативным временем отсутствия
--    поступления данных в БД АльфаЦЕНТР.
-- ===========================================================================
CREATE TABLE СтатусТУ (
    ИД_Статуса                  INTEGER     GENERATED ALWAYS AS IDENTITY
                                            PRIMARY KEY,
    Наименование                VARCHAR(50) NOT NULL,
    МаксВремяОтсутствияДанныхЧас INTEGER    NOT NULL,
    CONSTRAINT uq_статус_наименование UNIQUE (Наименование),
    CONSTRAINT chk_статус_максвремя_положительное
        CHECK (МаксВремяОтсутствияДанныхЧас > 0)
);

COMMENT ON TABLE  СтатусТУ IS 'Справочник статусов точек учёта';
COMMENT ON COLUMN СтатусТУ.ИД_Статуса IS 'Уникальный идентификатор статуса (суррогатный ПК)';
COMMENT ON COLUMN СтатусТУ.Наименование IS 'Наименование статуса (ОРЭМ, МТП, ДСД, ...)';
COMMENT ON COLUMN СтатусТУ.МаксВремяОтсутствияДанныхЧас IS 'Максимально допустимое время отсутствия данных (часы)';

-- ===========================================================================
-- 9. Таблица: ТочкаУчёта
--    Хранит точки учёта электрической энергии, идентифицируемые номером
--    присоединения/фидера в БД АльфаЦЕНТР.
-- ===========================================================================
CREATE TABLE ТочкаУчёта (
    ИД_ТУ                INTEGER      GENERATED ALWAYS AS IDENTITY
                                       PRIMARY KEY,
    Наименование         VARCHAR(255) NOT NULL,
    НомерПрисоединения   VARCHAR(50)  NOT NULL,
    ИД_Объекта           INTEGER      NOT NULL
                                       REFERENCES Объект(ИД_Объекта)
                                       ON DELETE CASCADE,
    ИД_Статуса           INTEGER      NOT NULL
                                       REFERENCES СтатусТУ(ИД_Статуса)
                                       ON DELETE RESTRICT,
    CONSTRAINT uq_ту_номер_присоединения UNIQUE (НомерПрисоединения)
);

COMMENT ON TABLE  ТочкаУчёта IS 'Точки учёта электрической энергии';
COMMENT ON COLUMN ТочкаУчёта.ИД_ТУ IS 'Уникальный идентификатор точки учёта (суррогатный ПК)';
COMMENT ON COLUMN ТочкаУчёта.Наименование IS 'Наименование точки учёта';
COMMENT ON COLUMN ТочкаУчёта.НомерПрисоединения IS 'Уникальный номер присоединения/фидера в БД АльфаЦЕНТР';
COMMENT ON COLUMN ТочкаУчёта.ИД_Объекта IS 'Ссылка на объект (ВК → Объект)';
COMMENT ON COLUMN ТочкаУчёта.ИД_Статуса IS 'Ссылка на статус ТУ (ВК → СтатусТУ)';

CREATE INDEX idx_ту_объект
    ON ТочкаУчёта(ИД_Объекта);
CREATE INDEX idx_ту_статус
    ON ТочкаУчёта(ИД_Статуса);
CREATE INDEX idx_ту_номер_присоединения
    ON ТочкаУчёта(НомерПрисоединения);

-- ===========================================================================
-- 10. Таблица: Заявка
--     Хранит заявки на выполнение работ по точкам учёта.
-- ===========================================================================
CREATE TABLE Заявка (
    ИД_Заявки      INTEGER        GENERATED ALWAYS AS IDENTITY
                                   PRIMARY KEY,
    ИД_ТУ          INTEGER        NOT NULL
                                   REFERENCES ТочкаУчёта(ИД_ТУ)
                                   ON DELETE CASCADE,
    НомерЗаявки    VARCHAR(100)   NOT NULL,
    ДатаНачала     DATE           NOT NULL,
    ДатаОкончания  DATE
);

COMMENT ON TABLE  Заявка IS 'Заявки на выполнение работ по точкам учёта';
COMMENT ON COLUMN Заявка.ИД_Заявки IS 'Уникальный идентификатор заявки (суррогатный ПК)';
COMMENT ON COLUMN Заявка.ИД_ТУ IS 'Ссылка на точку учёта (ВК → ТочкаУчёта)';
COMMENT ON COLUMN Заявка.НомерЗаявки IS 'Номер заявки';
COMMENT ON COLUMN Заявка.ДатаНачала IS 'Дата начала заявки';
COMMENT ON COLUMN Заявка.ДатаОкончания IS 'Дата окончания заявки';

CREATE INDEX idx_заявка_ту
    ON Заявка(ИД_ТУ);

-- ===========================================================================
-- 11. Таблица: Примечание
--     Хранит текстовые примечания по точкам учёта.
-- ===========================================================================
CREATE TABLE Примечание (
    ИД_Примечания  INTEGER        GENERATED ALWAYS AS IDENTITY
                                   PRIMARY KEY,
    ИД_ТУ          INTEGER        NOT NULL
                                   REFERENCES ТочкаУчёта(ИД_ТУ)
                                   ON DELETE CASCADE,
    Текст          TEXT           NOT NULL,
    ДатаСоздания   TIMESTAMP      DEFAULT CURRENT_TIMESTAMP
);

COMMENT ON TABLE  Примечание IS 'Текстовые примечания по точкам учёта';
COMMENT ON COLUMN Примечание.ИД_Примечания IS 'Уникальный идентификатор примечания (суррогатный ПК)';
COMMENT ON COLUMN Примечание.ИД_ТУ IS 'Ссылка на точку учёта (ВК → ТочкаУчёта)';
COMMENT ON COLUMN Примечание.Текст IS 'Текст примечания';
COMMENT ON COLUMN Примечание.ДатаСоздания IS 'Дата и время создания примечания (по умолчанию текущее)';

CREATE INDEX idx_примечание_ту
    ON Примечание(ИД_ТУ);

