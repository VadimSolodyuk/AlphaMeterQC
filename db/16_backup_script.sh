#!/bin/bash
# =============================================================================
# Скрипт резервного копирования справочной БД AlphaMeterQC
# СУБД: PostgreSQL 15+
# Автор: В.Л. Солодюк
# =============================================================================
# Скрипт выполняет полное резервное копирование БД alphameterqc в custom-формате
# с последующей ротацией (хранение 30 дней).
# =============================================================================

set -e

# =============================================================================
# Конфигурация
# =============================================================================

# Параметры подключения к БД
DB_HOST="localhost"
DB_PORT="5432"
DB_NAME="alphameterqc"
DB_USER="postgres"

# Директория для хранения резервных копий
BACKUP_DIR="/backup/alphameterqc"

# Срок хранения копий (дней)
RETENTION_DAYS=30

# Имя файла копии
TIMESTAMP=$(date +%Y%m%d_%H%M%S)
BACKUP_FILE="${BACKUP_DIR}/${DB_NAME}_${TIMESTAMP}.dump"

# Файл журнала
LOG_FILE="${BACKUP_DIR}/backup_${TIMESTAMP}.log"

# =============================================================================
# Функции
# =============================================================================

log() {
    echo "[$(date '+%Y-%m-%d %H:%M:%S')] $1" | tee -a "$LOG_FILE"
}

cleanup_old_backups() {
    log "Очистка копий старше ${RETENTION_DAYS} дней..."
    find "$BACKUP_DIR" -name "${DB_NAME}_*.dump" -type f -mtime +${RETENTION_DAYS} -delete
    find "$BACKUP_DIR" -name "backup_*.log" -type f -mtime +${RETENTION_DAYS} -delete
    log "Очистка завершена."
}

# =============================================================================
# Основной блок
# =============================================================================

# Создание директории для копий
mkdir -p "$BACKUP_DIR"

log "=== Начало резервного копирования ${DB_NAME} ==="
log "Целевой файл: ${BACKUP_FILE}"

# Проверка подключения к БД
if ! pg_isready -h "$DB_HOST" -p "$DB_PORT" > /dev/null 2>&1; then
    log "ОШИБКА: PostgreSQL не доступен на ${DB_HOST}:${DB_PORT}"
    exit 1
fi
log "Подключение к PostgreSQL установлено."

# Выполнение полного резервного копирования в custom-формате
log "Выполнение pg_dump..."
pg_dump -h "$DB_HOST" -p "$DB_PORT" -U "$DB_USER" -d "$DB_NAME" \
    --format=custom \
    --file="$BACKUP_FILE" \
    --verbose \
    --no-password 2>> "$LOG_FILE"

# Проверка результата
if [ $? -eq 0 ] && [ -f "$BACKUP_FILE" ]; then
    BACKUP_SIZE=$(du -h "$BACKUP_FILE" | cut -f1)
    log "Резервное копирование успешно завершено."
    log "Размер копии: ${BACKUP_SIZE}"
    log "Файл: ${BACKUP_FILE}"
else
    log "ОШИБКА: Резервное копирование не удалось."
    exit 1
fi

# Очистка старых копий
cleanup_old_backups

log "=== Резервное копирование завершено ==="
exit 0